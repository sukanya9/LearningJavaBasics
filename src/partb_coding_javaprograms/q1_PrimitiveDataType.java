package partb_coding_javaprograms;

public class q1_PrimitiveDataType {
    static int a;
    static float b;
    static boolean c;
    static byte d;
    static long l;
    static double e;
    static short s;
    static char p;
    public static void main(String[] args) {

        System.out.println("int default value" +a);
        System.out.println("Float default value"+b);
        System.out.println("boolean default value"+c);
        System.out.println("byte default value"+d);
        System.out.println("long default value"+l);
        System.out.println("double default value"+e);
        System.out.println("short default value"+s);
        System.out.println("char default value"+p);

    }
}
